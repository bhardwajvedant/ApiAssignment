package com.vedant;

public class LastClassParser {
private int mul1;
private int mul2;
private int mul3;
public LastClassParser(int mul1, int mul2, int mul3) {
	super();
	this.mul1 = mul1;
	this.mul2 = mul2;
	this.mul3 = mul3;
}
public LastClassParser() {
	super();
}
public int getMul1() {
	return mul1;
}
public void setMul1(int mul1) {
	this.mul1 = mul1;
}
public int getMul2() {
	return mul2;
}
public void setMul2(int mul2) {
	this.mul2 = mul2;
}
public int getMul3() {
	return mul3;
}
public void setMul3(int mul3) {
	this.mul3 = mul3;
}

}
